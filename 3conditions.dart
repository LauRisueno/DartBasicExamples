void main() {
  
  //Conditions
  bool birthday = true;
  if(birthday)
    print("Happy Birthday!!!");
  
  birthday = false;
  if(birthday == true)
    print("Happy Birthday!!");
  else
    print("Your birthday is not today :(");
  
  int number = 2;
  if(number == 2)
    print(number);
  
  //Change the value of number
  number = 3;
  if(number != 2)
    print("It is not equal 2");
  
  //Change the value of number again
  number = 10;
  
  if(number >= 15)
    print("Bigger or Equal 15");
  else if(number >= 5)
    print("Bigger or Equal 5");
  else if(number >= 2)
    print("Bigger or Equal 2");

}
